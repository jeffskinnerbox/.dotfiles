-- active/createcolorcode.lua

-- Create a color code by using the colorful display and sliders to easily
-- generate any desired color. Also color highlights text color codes
-- but these feature are redudent with nvim-colorizer.lua plugin.

-- Commandline
--    :CccPick  	                       - detects, displays, and replaces the color under the cursor
--    :CccHighlighterEnable [{bufnr}]    - highlight colors in the buffer {bufnr}, if {bufnr} is omitted, the target is the current buffer
--    :CccHighlighterDisable [{bufnr}]   - disable highlight
--    :CccHighlighterToggle [{bufnr}]    - toggle color code highlight on & off

-- Sources:
--    [GitHub: uga-rosa/ccc.nvim](https://github.com/uga-rosa/ccc.nvim)
--    [Documentation for Options, Commands, etc.](https://github.com/uga-rosa/ccc.nvim/blob/main/doc/ccc.txt)
--    [neovimcraft: uga-rosa/ccc.nvim](https://neovimcraft.com/plugin/uga-rosa/ccc.nvim/index.html)
--------------------------------------------------------------------------------


